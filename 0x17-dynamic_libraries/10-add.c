#include "holberton.h"

/**
 *add - adds two ints
 *@a: one int to add
 *@b: other int to add
 *Return: added ints
 */

int add(int a, int b)
{
	return (a + b);
}


/**
 *sub - subtracts two ints
 *@a: number 1
 *@b: number 2
 *Return: #1 - #2
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 *mul - multiplies two numbers
 *@a: #1
 *@b: #2
 *Return: a * b
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 *div - divides two numbers
 *@a: num 1
 *@b: num 2
 *Return: a / b
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 *mod - finds the remander of one number divided by another
 *@a: num1
 *@b: num2
 *Return: a % b
 */

int mod(int a, int b)
{
	return (a % b);
}
